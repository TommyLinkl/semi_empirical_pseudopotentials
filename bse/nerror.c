#include "fd.h"

/*****************************************************************************/

void nerror(char *str)
{
  FILE *pf;

  pf = fopen("error", "w");
  fprintf(pf, "%s\n", str);
  fclose(pf);

  exit(EXIT_FAILURE);
}

/*****************************************************************************/

void terminate(const char *msg) {
    fprintf(stderr, "%s\n", msg);
    exit(EXIT_FAILURE);
}

